export type I18nKey = keyof typeof DICT.en;

export function translate(lang: Lang, key: I18nKey) {
  const table = (DICT as unknown as Record<Lang, Record<I18nKey, string>>)[lang] ?? DICT.en;
  return (table[key] ?? DICT.en[key]) as string;
}

export function localeOf(lang: Lang) {
  return LANG_OPTIONS.find((x) => x.code === lang)?.locale ?? "en-US";
}
